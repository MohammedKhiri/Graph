import turtle
f=turtle.Turtle()
f.pensize(20)

f.pencolor("red")
f.circle(100)
f.right(100)

f.pencolor("blue")
f.circle(100)
f.left(100)
f.penup()
f.fd(220)
f.pendown()

f.pencolor("yellow")
f.circle(100)
f.right(100)

f.pencolor("black")
f.circle(100)

f.left(100)
f.penup()
f.fd(220)
f.pendown()

f.pencolor("green")
f.circle(100)
f.right(100)

turtle.done()
